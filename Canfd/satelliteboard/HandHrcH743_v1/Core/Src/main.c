/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_FDCAN1_Init();
  MX_TIM6_Init();
  MX_USART3_UART_Init();
  /* USER CODE BEGIN 2 */
  printf("Hello World\r\n");
  if (!CANFD_Utils_Init(&hfdcan1))
  {
    Error_Handler();
  }

  if (HAL_TIM_Base_Start_IT(&htim6) != HAL_OK)
  {
    Error_Handler();
  }

  // One-time CAN-FD health print (after FDCAN is started).
  {
    FDCAN_ProtocolStatusTypeDef ps;
    FDCAN_ErrorCountersTypeDef ec;
    memset(&ps, 0, sizeof(ps));
    memset(&ec, 0, sizeof(ec));
    if ((HAL_FDCAN_GetProtocolStatus(&hfdcan1, &ps) == HAL_OK) &&
        (HAL_FDCAN_GetErrorCounters(&hfdcan1, &ec) == HAL_OK))
    {
      printf("v1 CAN(init): BO=%lu W=%lu EP=%lu ACT=%lu LEC=%lu DLEC=%lu TEC=%lu REC=%lu\r\n",
             (unsigned long)ps.BusOff,
             (unsigned long)ps.Warning,
             (unsigned long)ps.ErrorPassive,
             (unsigned long)ps.Activity,
             (unsigned long)ps.LastErrorCode,
             (unsigned long)ps.DataLastErrorCode,
             (unsigned long)ec.TxErrorCnt,
             (unsigned long)ec.RxErrorCnt);
    }
    else
    {
      printf("v1 CAN(init): status read failed\r\n");
    }
  }

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    // NOTE: Do not spam UART prints in the main loop; it can drown out CAN RX prints
    // and distort timing. Keep prints event-driven (e.g., on RX or periodic heartbeat).

    if (CANFD_Utils_Consume1kHzTick())
    {
      // Send motor status for 15 motors each tick (motor_id 0..14), 1000 Hz.
      for (uint32_t motor_id = 0; motor_id < NUM_MOTORS; motor_id++)
      {
        Motor_TxPDO_t pdo = {0};
        pdo.motor_id = motor_id;
        // Dummy payload for bring-up; replace with real motor state later.
        pdo.present_id = (float)motor_id;
        pdo.present_iq = (float)((s_tx_counter + motor_id) % 1000U) * 0.01f;
        pdo.present_velocity = 12.34f;
        pdo.present_position = 56.78f;
        pdo.input_voltage = 24.0f;
        pdo.winding_temp = 40.0f;
        pdo.powerstage_temp = 42.0f;
        pdo.ic_temperature = 45.0f;
        pdo.error_status = 0.0f;

        (void)CANFD_Utils_SendMotorTxPDO(&hfdcan1, 0x200U + motor_id, &pdo);
      }
      s_tx_counter++;
    }

    // RX is handled in the interrupt: it updates g_robot_output.motor[motor_id] directly.
    // Optional debug print: show command values at a low rate.
    uint32_t now_ms = HAL_GetTick();
    if ((uint32_t)(now_ms - s_last_rx_print_ms) >= 200U) // 5 Hz
    {
      s_last_rx_print_ms = now_ms;
      const Motor_RxPDO_t cmd0 = g_robot_output.motor[0];
      union { float f; uint32_t u; } giq;
      giq.f = cmd0.goal_iq;
      printf("v1 CMD[0]: TE=%lu goal_iq_bits=0x%08lX\r\n",
             (unsigned long)cmd0.torque_enable,
             (unsigned long)giq.u);
    }

    // 1 Hz RX stats: how many frames we received in the last second.
    if ((uint32_t)(now_ms - s_last_stats_ms) >= 1000U)
    {
      s_last_stats_ms = now_ms;

      __disable_irq();
      uint32_t rx_total = g_rx_frames_total; g_rx_frames_total = 0;
      uint32_t rx_rxpdo = g_rx_frames_rxpdo; g_rx_frames_rxpdo = 0;
      uint32_t rx_other = g_rx_frames_other; g_rx_frames_other = 0;
      __enable_irq();

      printf("v1 RX/s: total=%lu rxpdo=%lu other=%lu\r\n",
             (unsigned long)rx_total,
             (unsigned long)rx_rxpdo,
             (unsigned long)rx_other);
    }
  }
  /* USER CODE END 3 */
}

